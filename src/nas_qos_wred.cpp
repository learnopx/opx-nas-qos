nas_qos_wred::nas_qos_wred (nas_qos_switch* switch_p)
           : base_obj_t (switch_p)
{
    memset(&cfg, 0, sizeof(cfg));
    wred_id = 0;
}

const nas_qos_switch& nas_qos_wred::get_switch()
{
    return static_cast<const nas_qos_switch&> (base_obj_t::get_switch());
}


